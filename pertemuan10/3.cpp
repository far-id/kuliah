#include <iostream>
using namespace std;

void multiply(int matrix1[9][9], int matrix2[9][9])
{
  int mult[9][9];
  for (int i = 0; i < 9; i++)
  {
    for (int j = 0; j < 9; j++)
    {
      mult[i][j] = matrix1[i][j] * matrix2[i][j];
    }
  }

  cout << "Hasil Perkalian [\n";
  for (int i = 0; i < 9; i++)
  {
    cout << "\t[";
    for (int j = 0; j < 9; j++)
    {
      cout << mult[i][j] << ", ";
    }
    cout << "]\n";
  }
  cout << "]\n";
}

